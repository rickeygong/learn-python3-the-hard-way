# 变量是用来存储数据的名称
# 你可以将值赋给变量，然后使用这些变量进行计算或操作

# 变量赋值
name = "龚东海"  # 字符串
age = 18  # 整数
height = 165.5  # 浮点数
is_student = False  # 布尔型

# 输出变量的值
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# 变量计算
new_age = age + 20
print("20年后的岁数是:", new_age)

# 变量交换
x = 10
y = 20
print("变量交换前: x =", x, ", y =", y)

# 交换 x 和 y 的值
x, y = y, x
print("变量交换后: x =", x, ", y =", y)
